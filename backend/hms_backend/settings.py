# Database Configuration
# Primary: PostgreSQL from .env
# Secondary Fallback: SQLite if PostgreSQL connection fails (e.g. offline dev, DB not created)
DATABASES = {
    'default': {
        'ENGINE': 'django.db.backends.sqlite3',
        'NAME': BASE_DIR / 'db.sqlite3',
    }
}

DB_ENGINE = os.getenv('DB_ENGINE')
DB_NAME = os.getenv('DB_NAME')
DB_USER = os.getenv('DB_USER')
DB_PASSWORD = os.getenv('DB_PASSWORD')
DB_HOST = os.getenv('DB_HOST')
DB_PORT = os.getenv('DB_PORT')

if DB_ENGINE and DB_NAME:
    try:
        import psycopg2
        # Verify connection can be opened
        conn = psycopg2.connect(
            dbname=DB_NAME,
            user=DB_USER,
            password=DB_PASSWORD,
            host=DB_HOST,
            port=DB_PORT,
            connect_timeout=2
        )
        conn.close()
        
        # If no error, configure database settings for PostgreSQL
        DATABASES['default'] = {
            'ENGINE': DB_ENGINE,
            'NAME': DB_NAME,
            'USER': DB_USER,
            'PASSWORD': DB_PASSWORD,
            'HOST': DB_HOST,
            'PORT': DB_PORT,
        }
        print("\n" + "="*80)
        print("[HMS SYSTEM] Successfully connected to PostgreSQL database.")
        print("="*80 + "\n")
    except Exception as e:
        print("\n" + "="*80)
        print(f"[HMS WARNING] Failed to connect to PostgreSQL: {e}")
        print("[HMS WARNING] Falling back to SQLite ('db.sqlite3') for smooth execution.")
        print("="*80 + "\n")


